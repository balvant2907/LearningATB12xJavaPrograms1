package ex_10_For_Loop;

public class Lab110_For_Loop_Find_Even_with_Continue {
    public static void main(String[] args) {
        for (int i = 0; i <50 ; i++) {
            if(i%2==0){
                System.out.println("it is even number" +i);
                continue;
            }


        }

    }
}
