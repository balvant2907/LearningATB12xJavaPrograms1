package ex_10_For_Loop;

public class Lab101_For_If_Loop {
    public static void main(String[] args) {
        for(int Anvika=0; Anvika <= 18; Anvika++){
            if(Anvika > 15){
                System.out.println("Papa will give new mobile to Anvika");
            }
            else{
                System.out.println("No mobile");
            }
        }
    }
}
