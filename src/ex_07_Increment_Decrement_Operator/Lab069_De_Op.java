package ex_07_Increment_Decrement_Operator;

public class Lab069_De_Op {
    public static void main(String[] args) {
        int a=10;
        int b= a++;
        System.out.println(a);
        System.out.println(b);

        int a_post=10;
        System.out.println(a_post++);
        System.out.println(a_post);

    }
}
